package com.myroom.dto;

public class UtilityInRoomItem {
    private long utilityKey;
    private String utilityId;
    private String utilityName;
    private String utilityFee;
    private String utilityIconName;

    public long getUtilityKey() {
        return utilityKey;
    }

    public void setUtilityKey(long utilityKey) {
        this.utilityKey = utilityKey;
    }

    public String getUtilityId() {
        return utilityId;
    }

    public void setUtilityId(String utilityId) {
        this.utilityId = utilityId;
    }

    public String getUtilityName() {
        return utilityName;
    }

    public void setUtilityName(String utilityName) {
        this.utilityName = utilityName;
    }

    public String getUtilityFee() {
        return utilityFee;
    }

    public void setUtilityFee(String utilityFee) {
        this.utilityFee = utilityFee;
    }

    public String getUtilityIconName() {
        return utilityIconName;
    }

    public void setUtilityIconName(String utilityIconName) {
        this.utilityIconName = utilityIconName;
    }
}
